# Convert int to float
a=5
b=float(a)
print(b) # Output: 5.0

# Convert float to int
x=5.5
y=int(x)
print(y) # Output: 5

# Convert int to string
a=5
b=str(a)
print(b) # Output: '5'

# Convert string to int
s="10"
a=int(s)
print(a) # Output: 10

# Convert string to float
s="10.5"
a=float(s)
