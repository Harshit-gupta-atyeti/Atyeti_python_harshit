# The break statement is used to exit a loop prematurely.
'''
num=[1,2,3,4,5]
for i in num:
    if i == 3:
        break
    print(i)
'''
# The continue statement is used to skip the current iteration and move to the next iteration.
'''
num=[1,2,3,4,5]
for i in num:
    if i%2==0:
        continue
    print(i)
'''
# The pass statement is a null operation; it is a placeholder for future code.
'''
num=[1,2,3,4,5]
for i in num:
    if i == 3:
        pass
    print(i)
'''